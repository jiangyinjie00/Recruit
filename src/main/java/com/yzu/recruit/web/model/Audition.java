package com.yzu.recruit.web.model;

public class Audition {
    private int[] jobRequestArrary;
    private String auditionTime;
    private String auditionInfo;
    public int[] getJobRequestArrary() {
        return jobRequestArrary;
    }
    public void setJobRequestArrary(int[] jobRequestArrary) {
        this.jobRequestArrary = jobRequestArrary;
    }
    public String getAuditionTime() {
        return auditionTime;
    }
    public void setAuditionTime(String auditionTime) {
        this.auditionTime = auditionTime;
    }
    public String getAuditionInfo() {
        return auditionInfo;
    }
    public void setAuditionInfo(String auditionInfo) {
        this.auditionInfo = auditionInfo;
    }


}
